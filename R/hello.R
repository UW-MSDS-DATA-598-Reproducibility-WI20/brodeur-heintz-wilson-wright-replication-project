#' Add
#'
#' Adds two numbers
#'
#' @param a the first number
#' @param b the second number
#'
#' @return The sum of two numbers
#' @export
#'
#' @examples
#' add(2, 3)
add <- function(a, b) {
  return(a + b);
}

#' Substracts two numbers
#'
#' @param a the first number
#' @param b the number to substract
#'
#' @return The difference of two numbers (a-b)
#' @export
#'
#' @examples
#' substract(4, 2)
substract <- function(a, b) {
  return(a - b);
}
